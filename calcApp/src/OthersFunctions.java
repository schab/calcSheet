public class OthersFunctions {

    public OthersFunctions() {
    }

    public double Potegowanie(double a, double b){

        return Math.pow(a, b);
    }

    public double Pierwiastkowanie(double a){

        return Math.sqrt(a);
    }

    public double Logarytmowanie(double a){

        return Math.log(a);
    }
}
