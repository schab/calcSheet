public class Arithmetic {

    public Arithmetic() {
    }

    public double Dodawanie(double a, double b){

       return a + b;
    }

    public double Odejmowanie(double a, double b){

        return a - b;
    }

    public double Mnozenie(double a, double b){

        return a * b;
    }

    public double Dzielenie(double a, double b){

        return a/b;
    }
}
